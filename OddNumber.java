public class OddNumber {

	public static int count = 0;

	public static void main(String args[]) {
		int a = 8, b = 3, c = 10;

		if (((a % 2) == 0)) {
			count++;
		}

		if (((b % 2) == 0)) {
			count++;
		}

		if (((c % 2) == 0)) {
			count++;
		}
		System.out.println("The number of odd numbers is " + count);
	}
}

	
			 



	
	 
	
	
	
	


	

 
